# Print statement calculating the number of hens
print("Hens", 25.0 + 30.0 / 6.0) #30
# Print statement calculating the number of roosters
print("Roosters", 100.0 - 25.0 * 3.0 % 4.0) #1

# A remark now calculating the eggs
print("Now I will count the eggs:")

# Print statement calculating the number of roosters
print(3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0)

# Another remark
print("Is it true that 3 + 2 < 5 - 7?") # False

# Print statement of result from comparison operation
print(3.0 + 2.0 < 5.0 - 7.0)

# Print statement from above comparison operation, left hand side (addition)
print("What is 3 + 2?", 3.0 + 2.0)
# Print statement from above comparison operation, right hand side (subtraction)
print("What is 5 - 7?", 5.0 - 7.0)

# Print statement, simple remark
print("Oh, that's why it's False.")

# Print statement, simple remark
print("How about some more.")

# another print statement on the result of comparison operation, greater-than
print("Is it greater?", 5.0 > -2.0) # True
# another statement on the result of comparison operation, greater-or-equal-to
print("Is it greater or equal?", 5.0 >= -2.0) # True
# another statement on the result of comparison operation, less-or-equal-to
print("Is it less or equal?", 5.0 <= -2.0) # False
